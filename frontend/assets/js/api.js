const API_URL = window.location.origin;

const api = {
  token: localStorage.getItem('flyertrack_token'),
  refreshToken: localStorage.getItem('flyertrack_refresh'),

  setTokens(access, refresh) {
    this.token = access;
    this.refreshToken = refresh;
    localStorage.setItem('flyertrack_token', access);
    localStorage.setItem('flyertrack_refresh', refresh);
  },

  clearTokens() {
    this.token = null;
    this.refreshToken = null;
    localStorage.removeItem('flyertrack_token');
    localStorage.removeItem('flyertrack_refresh');
  },

  async request(method, endpoint, body = null, isFormData = false) {
    const headers = {};
    if (this.token) headers['Authorization'] = `Bearer ${this.token}`;
    if (!isFormData) headers['Content-Type'] = 'application/json';

    const options = { method, headers };
    if (body) options.body = isFormData ? body : JSON.stringify(body);

    let res = await fetch(`${API_URL}${endpoint}`, options);

    if (res.status === 403 && this.refreshToken) {
      const refreshRes = await fetch(`${API_URL}/api/auth/refresh`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ refreshToken: this.refreshToken })
      });

      if (refreshRes.ok) {
        const data = await refreshRes.json();
        this.setTokens(data.accessToken, data.refreshToken);
        headers['Authorization'] = `Bearer ${this.token}`;
        res = await fetch(`${API_URL}${endpoint}`, { method, headers, body: options.body });
      } else {
        this.clearTokens();
	window.location.href = '/login.html';
        return;
      }
    }

    if (res.status === 401) {
      const data = await res.json();
      throw new Error(data.error || 'Credenciales incorrectas');
    }

    const contentType = res.headers.get('content-type');
    if (contentType && contentType.includes('application/json')) {
      const data = await res.json();
      if (!res.ok) throw new Error(data.error || 'Error en la petición');
      return data;
    }

    if (!res.ok) throw new Error('Error en la petición');
    return res;
  },

  get(endpoint) { return this.request('GET', endpoint); },
  post(endpoint, body) { return this.request('POST', endpoint, body); },
  put(endpoint, body) { return this.request('PUT', endpoint, body); },
  delete(endpoint) { return this.request('DELETE', endpoint); },
  upload(endpoint, formData) { return this.request('POST', endpoint, formData, true); },

  async login(usuario, password) {
    const data = await this.post('/api/auth/login', { usuario, password });
    this.setTokens(data.accessToken, data.refreshToken);
    return data;
  },

  async logout() {
    await this.post('/api/auth/logout', { refreshToken: this.refreshToken });
    this.clearTokens();
  },

  isLoggedIn() { return !!this.token; },

  descargarPDF(eventoId) {
    window.open(`${API_URL}/api/informes/pdf/${eventoId}?token=${this.token}`, '_blank');
  },

  descargarCSV(eventoId) {
    window.open(`${API_URL}/api/informes/csv/${eventoId}?token=${this.token}`, '_blank');
  }
};

// Escapes HTML special characters to prevent XSS when interpolating
// user-supplied free-text values into innerHTML/template literals.
// Escaping order matters: '&' must be escaped first to avoid double-escaping
// the entities generated by the other replacements.
function escapeHtml(str) {
  if (str === null || str === undefined) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
window.escapeHtml = escapeHtml;

const showAlert = (message, type = 'danger', container = 'alert-container') => {
  const el = document.getElementById(container);
  if (!el) return;
  el.innerHTML = `<div class="alert alert-${type}">${message}</div>`;
};

const getInitials = (name) => {
  return name.split(' ').map(n => n[0]).join('').substring(0, 2).toUpperCase();
};

const formatDate = (date) => {
  return new Date(date).toLocaleDateString('es-ES', {
    day: '2-digit', month: '2-digit', year: 'numeric'
  });
};

const formatDateTime = (date) => {
  return new Date(date).toLocaleString('es-ES', {
    day: '2-digit', month: '2-digit', year: 'numeric',
    hour: '2-digit', minute: '2-digit'
  });
};

